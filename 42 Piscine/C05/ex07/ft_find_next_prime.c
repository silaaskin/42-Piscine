int	ft_find_next_prime(int nb)
{
	int	i;
	int	j;

	i = 2;
	j = nb;
	if (nb <= 1)
		j = 2;
	while (i < j)
	{
		if (j % i == 0)
		{
			j++;
			i = 2;
		}
		i++;
	}
	return (j);
}
